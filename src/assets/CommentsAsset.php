namespace lispa\amos\comments\assets;

use yii\web\AssetBundle;

/**
 * Class CommentsAsset
 * @package lispa\amos\comments\assets
 */
class CommentsAsset extends AssetBundle
{
    /**
     * @inheritdoc
     */
    public $sourcePath = '@vendor/lispa/amos-comments/src/assets/web';

    /**
     * @inheritdoc
     */
    public $css = [
        'less/comments.less'
    ];

    /**
     * @inheritdoc
     */
    public $js = [
        'js/comments.js'
    ];

    /**
     * @inheritdoc
     */
    public $depends = [
        'yii\web\JqueryAsset',
    ];

    public function init()
    {
        $moduleL = \Yii::$app->getModule('layout');
        if (!empty($moduleL)) {
            $this->depends [] = 'lispa\amos\layout\assets\BaseAsset';
        } else {
            $this->depends [] = 'lispa\amos\core\views\assets\AmosCoreAsset';
        }
        parent::init(); // TODO: Change the autogenerated stub
    }
}
